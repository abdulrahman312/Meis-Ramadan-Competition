export type Language = 'ar' | 'en';

export const getHijriDate = (lang: Language) => {
  try {
    return new Intl.DateTimeFormat(lang === 'ar' ? 'ar-SA' : 'en-US-u-ca-islamic', {
      calendar: 'islamic',
      day: 'numeric',
      month: 'long',
      year: 'numeric'
    }).format(new Date());
  } catch (e) {
    return "Ramadan 1446";
  }
};

